#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		if ((*lst)->content)
			del((*lst)->content);
		temp = *lst;
		*lst = temp->next;
		free(temp);
	}
	lst = NULL;
}
