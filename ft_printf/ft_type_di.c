#include "ft_printf.h"

int	ft_type_di(int a)
{
	int	len;

	len = 0;
	if (a == 0)
		return (write(1, "0", 1));
	if (a == -2147483648)
		return (write(1, "-2147483648", 11));
	if (a < 0)
	{
		len += write(1, "-", 1);
		a *= -1;
	}
	if (a >= 10)
		len += ft_type_di(a / 10);
	write(1, &"0123456789"[a % 10], 1);
	return (len + 1);
}
