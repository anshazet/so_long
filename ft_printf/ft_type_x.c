#include "ft_printf.h"

int	ft_type_x(unsigned int a, char c)
{
	int	len;

	len = 0;
	if (a >= 16)
		len += ft_type_x(a / 16, c);
	if (c == 'x')
		write(1, &"0123456789abcdef"[a % 16], 1);
	if (c == 'X')
		write(1, &"0123456789ABCDEF"[a % 16], 1);
	return (len + 1);
}
