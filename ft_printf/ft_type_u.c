#include "ft_printf.h"

int	ft_type_u(unsigned int a)
{
	int	len;

	len = 0;
	if (a >= 10)
		len += ft_type_u(a / 10);
	write(1, &"0123456789"[a % 10], 1);
	return (len + 1);
}
